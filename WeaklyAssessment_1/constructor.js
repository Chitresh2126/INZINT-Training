class Details {
    constructor() {
        this.name = 'Ramesh',
        this.age = 19,
        this.gender = 'M',
        this.skill = 'Developer';
    }
}
const detail = new Details();
console.log(detail.age);
console.log(detail.name);
console.log(detail.gender);
console.log(detail.skill);


